public class DataTypes {
	
	public static void main(String args[]) {
		
	int a = 10;
	int b= 20;
	int temp=0;
	
	
	System.out.println("Value of a = "+a);
	System.out.println("Value of b = "+b);
	
	
	// Swapping Logic
	
	temp = a;
	a=b;
	b=temp;
	
	
	System.out.println("Value of a = "+a);
	System.out.println("Value of b = "+b);
	
	
		
		
		
		
	}

}
